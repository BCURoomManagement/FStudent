package com.overseas.entity;

public class Proficiency {
    private String username;
    private String proficiency_c;
    private String level_c;
    private String proficiency_e;
    private String level_e;

      public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getProficiency_c() {
        return proficiency_c;
    }

    public void setProficiency_c(String proficiency_c) {
        this.proficiency_c = proficiency_c;
    }

    public String getLevel_c() {
        return level_c;
    }

    public void setLevel_c(String level_c) {
        this.level_c = level_c;
    }

    public String getProficiency_e() {
        return proficiency_e;
    }

    public void setProficiency_e(String proficiency_e) {
        this.proficiency_e = proficiency_e;
    }

    public String getLevel_e() {
        return level_e;
    }

    public void setLevel_e(String level_e) {
        this.level_e = level_e;
    }
}
